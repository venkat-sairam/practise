with cte as
(
  
  select *
  , rank() over(partition by state order by population) as l_rnk
  , rank() over(partition by state order by population desc) as h_rnk
  from city_population
)

select 
state
, min(case when l_rnk = 1 then city end ) as l_pop
, min(case when h_rnk = 1 then city end ) as l_pop
from cte
group by state
  ;
